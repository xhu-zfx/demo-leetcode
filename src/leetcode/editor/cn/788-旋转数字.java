    Boolean[] must= {true,true,true,false,false,true,true,false,true,true};
    Boolean[] exist= {false,false,true,false,false,true,true,false,false,true};
    public int rotatedDigits(int n) {
        int count=0;
        for (int i = 1; i <= n; i++) {
            if (isGood(i)) count++;
        }
        return count;
    }
